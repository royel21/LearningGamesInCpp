#pragma once
#include <memory>

using u32 = unsigned int;
using u8 = unsigned char;

namespace Plutus {

    template<typename T>
    using Ref = std::shared_ptr<T>;

    template<typename T, typename ... Args>
    constexpr Ref<T> CreateRef(Args&& ... args)
    {
        return std::make_shared<T>(std::forward<Args>(args)...);
    }
}